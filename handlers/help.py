# =========================
# 🔙 Клавиатура «назад» для разделов
# =========================
def kb_back_to_help() -> InlineKeyboardMarkup:
    return InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="⬅️ Назад", callback_data="help_main")]
    ])

def kb_back_to_faq() -> InlineKeyboardMarkup:
    return InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="⬅️ К вопросам", callback_data="help_faq")],
        [InlineKeyboardButton(text="🏠 Главная справка", callback_data="help_main")],
    ])


# =========================
# /help — точка входа
# =========================
@router.message(Command("help"))
async def cmd_help(message: Message):
    await message.answer(
        HELP_MAIN_TEXT,
        reply_markup=kb_help_main(),
        parse_mode='HTML'
    )


# =========================
# Главное меню help (через кнопку)
# =========================
@router.callback_query(F.data == "help_main")
async def help_main(callback: CallbackQuery):
    await callback.answer()
    await safe_edit(callback.message, HELP_MAIN_TEXT, kb_help_main())


# =========================
# Разделы справки
# =========================
@router.callback_query(F.data.in_(SECTIONS.keys()))
async def help_section(callback: CallbackQuery):
    await callback.answer()
    text = SECTIONS[callback.data]
    await safe_edit(callback.message, text, kb_back_to_help())


# =========================
# FAQ — список вопросов
# =========================
@router.callback_query(F.data == "help_faq")
async def help_faq(callback: CallbackQuery):
    await callback.answer()
    await safe_edit(
        callback.message,
        "❓ <b>Частые вопросы (FAQ)</b>\n\nВыбери вопрос:",
        kb_faq()
    )


# =========================
# FAQ — ответы
# =========================
@router.callback_query(F.data.in_(FAQ.keys()))
async def faq_answer(callback: CallbackQuery):
    await callback.answer()
    text = FAQ[callback.data]
    await safe_edit(callback.message, text, kb_back_to_faq())
